import java.util.Scanner;

public class 문제_1_1 {
  public static void main(String[] args) {
    int num1, num2;

    num1 = 49;
    num2 = 32;

    if(num1 + num2 >= 50){
      System.out.println("두 수의 합이 50보다 큽니다.");
    }
    if(num1 + num2 <= 50){
      System.out.println("두 수의 합이 50보다 작습니다.");
    }
  }
}
